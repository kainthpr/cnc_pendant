uint8_t process_command(char* cmd)
{

	int c1;
	char buffer[MSG_LENGTH];

	c1 = atoi(strtok(cmd, " "));
//    c2 = atoi(strtok(NULL,  " "));
//    c3 = atoi(strtok(NULL,  " "));

    if (c1 == 990)  // connection check
    {
    	connected = 1;
    	send_back("Pendant\r\n");
    }
    else if (c1 == 991)  // fw version
    {
    	snprintf(buffer, sizeof(buffer), "%s\r\n", version);
    	send_back(buffer);
    }
    else if (c1 == 993) // codes requested
    {
    	snprintf(buffer, sizeof(buffer), "%s\r\n", version);
    	send_back(buffer);
    }

    else
    {
    	return 1;
    }
    return 0;
}
